package Human;

public class Leg {
	private double girth;
	private double length;
	public void setgirth(double girth) {
		this.girth = girth;
	}
	public void setlength(double length) {
		this.length = length;
	}
	public double getgirth() {
		return girth;
	}
	public double getlength() {
		return length;
	}
}
